/*
COMPILE TIME - When you writing the code
RUN TIME -  When the code is being executed

Scanner class
-------------

- Predefined class
- It is used to get input from user in run time and assign it to a variable
- It is present in java.util package (default java package is java.lang )

USER DEFINED VS PREDIFINED - How to predict : Just think who is going to write the code inside the method.
						If it is going to be written by user, it is userdefined. 
  
  Syntax
  ------

  Scanner <objref> = new Scanner(System.in);
  <objref>.<scannerMethods>();

  Scanner Methods : 
  -----------------

 1) nextByte();  --> Accepting Byte

 2) nextShort();  ---> Accepting Short

 3) nextInt();  -----> Accepting Int

 4) nextLong();   ----> Accepting Long

 5) nextDouble();    ----> Accepting Double

 6) nextFloat();     ----> Accepting Float

 7) nextBoolean();    ------> Accepting Boolean

 8) next();    ----> Accepting string without a space

 9) nextLine();   ---> Accepting string with space
 */